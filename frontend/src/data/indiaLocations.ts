export const getDistrictsByState = (stateCode: string): District[] => {
  const state = INDIAN_STATES.find((s) => s.code === stateCode);
  return state?.districts || [];
};

export const getStateName = (stateCode: string): string => {
  const state = INDIAN_STATES.find((s) => s.code === stateCode);
  return state?.name || '';
};

export const getDistrictName = (stateCode: string, districtCode: string): string => {
  const state = INDIAN_STATES.find((s) => s.code === stateCode);
  const district = state?.districts.find((d) => d.code === districtCode);
  return district?.name || '';
};
